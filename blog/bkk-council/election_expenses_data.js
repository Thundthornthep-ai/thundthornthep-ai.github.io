  function _calcCountdown(targetDateStr) {
    var now = new Date();
    var target = new Date(targetDateStr + "T00:00:00+07:00");
    var diff = target - now;
    if (diff <= 0) {
      return { isPast: true, days: 0, hours: 0, minutes: 0, seconds: 0 };
    }
    var totalSec = Math.floor(diff / 1000);
    return {
      isPast: false,
      days: Math.floor(totalSec / 86400),
      hours: Math.floor((totalSec % 86400) / 3600),
      minutes: Math.floor((totalSec % 3600) / 60),
      seconds: totalSec % 60
    };
  }

  function _formatThaiDate(dateStr) {
    var months = ["ม.ค.", "ก.พ.", "มี.ค.", "เม.ย.", "พ.ค.", "มิ.ย.", "ก.ค.", "ส.ค.", "ก.ย.", "ต.ค.", "พ.ย.", "ธ.ค."];
    var d = new Date(dateStr + "T00:00:00Z");
    return d.getUTCDate() + " " + months[d.getUTCMonth()] + " " + (d.getUTCFullYear() + 543);
  }

